def get_token_supply_etherscan(token_address):
    """L·∫•y total supply t·ª´ Etherscan v·ªõi error handling t·ªët h∆°n"""
    api_key = get_etherscan_api_key()
    if not api_key:
        return None
        
    try:
        url = f"https://api.etherscan.io/api?module=stats&action=tokensupply&contractaddress={token_address}&apikey={api_key}"
        
        # Th√™m delay ƒë·ªÉ tr√°nh rate limit
        time.sleep(0.3)
        
        response = requests.get(url, timeout=15)
        
        if response.status_code == 200:
            data = response.json()
            
            if data['status'] == '1':
                supply = int(data['result'])
                return supply
            else:
                error_msg = data.get('message', 'Unknown error')
                st.warning(f"‚ö†Ô∏è Etherscan API Error: {error_msg}")
                
                # G·ª£i √Ω fix d·ª±a tr√™n error message
                if 'rate limit' in error_msg.lower():
                    st.info("üí° G·ª£i √Ω: ƒê·ª£i 1-2 ph√∫t r·ªìi th·ª≠ l·∫°i (rate limit)")
                elif 'invalid API Key' in error_msg:
                    st.error("‚ùå API Key kh√¥ng h·ª£p l·ªá. Ki·ªÉm tra l·∫°i trong Streamlit secrets.")
                elif 'invalid address' in error_msg:
                    st.error("‚ùå Contract address kh√¥ng h·ª£p l·ªá.")
                    
                return None
        else:
            st.error(f"‚ùå HTTP Error: {response.status_code}")
            return None
            
    except requests.exceptions.Timeout:
        st.error("‚è∞ Timeout khi k·∫øt n·ªëi ƒë·∫øn Etherscan API")
        return None
    except Exception as e:
        st.error(f"‚ùå L·ªói: {e}")
        return None

def get_token_holders_etherscan_safe(token_address, max_holders=50):
    """L·∫•y holders an to√†n v·ªõi rate limiting"""
    api_key = get_etherscan_api_key()
    if not api_key:
        return None
        
    try:
        # Ch·ªâ l·∫•y 50 holders ƒë·∫ßu ti√™n ƒë·ªÉ test
        url = f"https://api.etherscan.io/api?module=token&action=tokenholderlist&contractaddress={token_address}&page=1&offset={max_holders}&apikey={api_key}"
        
        # Th√™m delay ƒë·ªÉ tr√°nh rate limit
        time.sleep(0.5)
        
        response = requests.get(url, timeout=20)
        
        if response.status_code == 200:
            data = response.json()
            
            if data['status'] == '1':
                holders = data['result']
                if holders and len(holders) > 0:
                    return holders
                else:
                    st.warning("‚ö†Ô∏è Token c√≥ v·∫ª nh∆∞ kh√¥ng c√≥ holders ho·∫∑c d·ªØ li·ªáu tr·ªëng")
                    return None
            else:
                error_msg = data.get('message', 'Unknown error')
                st.warning(f"‚ö†Ô∏è Kh√¥ng th·ªÉ l·∫•y holders: {error_msg}")
                return None
        else:
            st.error(f"‚ùå HTTP Error: {response.status_code}")
            return None
            
    except Exception as e:
        st.error(f"‚ùå L·ªói khi l·∫•y holders: {e}")
        return None

